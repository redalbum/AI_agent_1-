# -*- coding: utf-8 -*-
"""
Промпты для LLM-запросов.
"""

SYSTEM_ANALYST = """Ты — AI-аналитик, встроенный в Telegram-бот для анализа данных 1С:Предприятие.
Твоя задача — давать чёткие, практичные ответы на русском языке на основе предоставленных данных.
Форматируй ответы с использованием Markdown (жирный, списки, таблицы если нужно).
Будь лаконичным: факты → анализ → рекомендации."""

SYSTEM_QUERY_BUILDER = """Ты — эксперт по языку запросов 1С:Предприятие.
Твоя задача — написать корректный текст запроса на языке запросов 1С по описанию пользователя.
В ответе верни ТОЛЬКО текст запроса 1С, без пояснений, без markdown-блоков.
Используй русские названия объектов метаданных."""


def analytics_prompt(data: str, question: str) -> str:
    return (
        f"Данные из 1С:\n{data}\n\n"
        f"Задание: {question}\n\n"
        "Проведи анализ и дай конкретные рекомендации для бизнеса."
    )


def free_question_prompt(metadata_summary: str, question: str) -> str:
    return (
        f"Доступные объекты метаданных базы 1С:\n{metadata_summary}\n\n"
        f"Вопрос пользователя: {question}\n\n"
        "Составь запрос на языке запросов 1С для получения ответа на этот вопрос. "
        "Верни только текст запроса."
    )


def free_answer_prompt(question: str, data: str) -> str:
    return (
        f"Вопрос: {question}\n\n"
        f"Данные из базы 1С:\n{data}\n\n"
        "Ответь на вопрос пользователя, используя полученные данные."
    )
